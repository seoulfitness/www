    USER_ID VARCHAR(50) PRIMARY KEY COMMENT '아이디',
    PASSWORD VARCHAR(255) NOT NULL COMMENT '비밀번호',
    USER_NAME VARCHAR(100) NOT NULL COMMENT '이름',
