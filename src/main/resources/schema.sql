    PROVINCE_ID INT AUTO_INCREMENT PRIMARY KEY COMMENT '시/도 아이디',
    PROVINCE_NAME VARCHAR(50) NOT NULL COMMENT '시/도 이름',
    PROVINCE_MEMO VARCHAR(255) COMMENT '시/도 메모',
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
    UPDATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    CREATED_BY VARCHAR(50) COMMENT '등록한 사람',
    UPDATED_BY VARCHAR(50) COMMENT '수정한 사람'
);

-- 구/군 테이블
