-- 대학교 테이블
CREATE TABLE IF NOT EXISTS SCHOOLS (
    SCHOOL_ID INT AUTO_INCREMENT PRIMARY KEY COMMENT '대학교 아이디',
    SCHOOL_NAME VARCHAR(50) COMMENT '대학교 이름',
    SCHOOL_ADDRESS VARCHAR(255) COMMENT '대학교 주소',
    SCHOOL_PHONE VARCHAR(20) COMMENT '대학교 전화번호',
    SCHOOL_URL VARCHAR(255) COMMENT '대학교 웹사이트',
    ADMISSION_INFO_URL VARCHAR(255) COMMENT '대학교 입학안내',
    SCHOOL_LOGO_URL VARCHAR(255) COMMENT '대학교 로고 이미지 경로',
    SCHOOL_LOGO_FILENAME VARCHAR(255) COMMENT '대학교 로고 이미지 파일명',
    SCHOOL_LOGO_ORIGINAL_FILENAME VARCHAR(255) COMMENT '대학교 로고 이미지 원본 파일명',
    SCHOOL_LOGO VARCHAR(255) NOT NULL DEFAULT 'none' COMMENT '대학교 로고 선택',
    SCHOOL_MEMO VARCHAR(255) COMMENT '대학교 메모',
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
    UPDATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
