-- 대학교 테이블
CREATE TABLE IF NOT EXISTS SCHOOLS (
    SCHOOL_ID INT AUTO_INCREMENT PRIMARY KEY COMMENT '대학교 아이디',
    SCHOOL_NAME VARCHAR(50) COMMENT '대학교 이름',
    SCHOOL_ADDRESS VARCHAR(255) COMMENT '대학교 주소',
    SCHOOL_PHONE VARCHAR(20) COMMENT '대학교 전화번호',
    SCHOOL_WEBSITE VARCHAR(255) COMMENT '대학교 웹사이트',
