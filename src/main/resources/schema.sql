    GRADE3_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '3등급(-100.000 ~ 100.000)',
    GRADE4_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '4등급(-100.000 ~ 100.000)',
    GRADE5_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '5등급(-100.000 ~ 100.000)',
    GRADE6_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '6등급(-100.000 ~ 100.000)',
    GRADE7_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '7등급(-100.000 ~ 100.000)',
    GRADE8_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '8등급(-100.000 ~ 100.000)',
    GRADE9_REFLECTED_SCORE DECIMAL(6, 3) COMMENT '9등급(-100.000 ~ 100.000)',
    REGULAR_ADMISSION_ENGLISH_MEMO TEXT COMMENT '정시 영어 등급별 점수 메모',
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
    UPDATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    CREATED_BY VARCHAR(50) COMMENT '등록한 사람',
    UPDATED_BY VARCHAR(50) COMMENT '수정한 사람'
